#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	rs;
	int	sign;

	if (str[0] == '\0')
		return (0);
	i = 0;
	rs = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rs = rs * 10 + (str[i] - '0');
		i++;
	}
	return (rs * sign);
}
